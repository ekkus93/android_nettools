/**
 * Supplies the current set of supported curl options used for pre-run validation.
 *
 * This catalog is intentionally injected so it can later be swapped to native
 * metadata derived from the embedded curl build.
 */
interface CurlOptionCatalog {
    /** Returns the option names accepted by the bundled curl runtime. */
    fun supportedOptions(): Set<String>
}

